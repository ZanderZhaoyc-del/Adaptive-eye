const DEFAULT_SUMMARY = {
  totalElements: 0,
  issuesFound: 0,
  criticalIssues: 0,
  warningIssues: 0,
  passCount: 0
};

export function normalizeAuditResult(rawResult, options = {}) {
  const status = rawResult?.status || 'error';
  const summary = { ...DEFAULT_SUMMARY, ...(rawResult?.summary || {}) };
  const findings = Array.isArray(rawResult?.issues)
    ? rawResult.issues.map(normalizeFinding)
    : [];

  return {
    auditType: 'contrast',
    source: options.source || 'dom',
    confidence: options.confidence || (status === 'success' ? 'high' : 'low'),
    status,
    pageUrl: rawResult?.pageUrl || options.pageUrl || '',
    pageTitle: rawResult?.pageTitle || options.pageTitle || 'Untitled Page',
    pageDimensions: rawResult?.pageDimensions || options.pageDimensions,
    generatedAt: options.generatedAt || new Date().toISOString(),
    summary,
    findings,
    errorMessage: rawResult?.errorMessage || ''
  };
}

export function generateContrastMarkdownReport(result) {
  const criticalFindings = result.findings.filter((finding) => finding.severity === 'critical');
  const warningFindings = result.findings.filter((finding) => finding.severity === 'warning');

  return [
    `# Contrast Audit Report: ${result.pageTitle || 'Untitled Page'}`,
    '',
    `**URL:** ${result.pageUrl || 'Unknown'}`,
    `**Date:** ${result.generatedAt}`,
    `**Source:** ${result.source}`,
    `**Confidence:** ${result.confidence}`,
    '',
    '## Summary',
    '',
    '| Metric | Count |',
    '|--------|-------|',
    `| Total Elements Scanned | ${result.summary.totalElements} |`,
    `| Issues Found | ${result.summary.issuesFound} |`,
    `| Critical (< 3:1) | ${result.summary.criticalIssues} |`,
    `| Warning (3:1 - 4.5:1) | ${result.summary.warningIssues} |`,
    `| Pass (WCAG AA) | ${result.summary.passCount} |`,
    '',
    '## Critical Issues',
    '',
    formatFindingsTable(criticalFindings),
    '',
    '## Warning Issues',
    '',
    formatFindingsTable(warningFindings),
    '',
    '## WCAG Thresholds Reference',
    '',
    '| Text Size | AA (Minimum) | AAA (Enhanced) |',
    '|-----------|--------------|----------------|',
    '| Normal text (< 18px, < 14px bold) | 4.5:1 | 7:1 |',
    '| Large text (>= 18px or >= 14px bold) | 3:1 | 4.5:1 |',
    '',
    '---',
    '',
    '*Report generated by adaptive-eye-cli.*',
    ''
  ].join('\n');
}

export function buildReportFileName(pageUrl, date = new Date()) {
  const host = safeHostName(pageUrl);
  const timestamp = date.toISOString()
    .replace(/\.\d{3}Z$/, '')
    .replace('T', '-')
    .replaceAll(':', '');

  return `contrast-report-${host}-${timestamp}.md`;
}

export function buildThemeBatchIndex({
  pageUrl,
  generatedAt,
  requestedThemes,
  executedThemes,
  originalTheme,
  restoredOriginalTheme,
  restoreError = '',
  results
}) {
  return {
    auditType: 'contrast-theme-batch',
    pageUrl,
    generatedAt,
    platform: process.platform,
    requestedThemes,
    executedThemes,
    restoredOriginalTheme,
    originalTheme,
    restoreError,
    results
  };
}

export function generateThemeBatchMarkdownReport(batch) {
  const rows = [
    '| Theme | Result | Issues Found | Critical | Warning | JSON Report | Markdown Report | Annotated Screenshot |',
    '|-------|--------|--------------|----------|---------|-------------|-----------------|----------------------|'
  ];

  batch.results.forEach((result) => {
    rows.push([
      result.theme,
      result.status,
      result.summary?.issuesFound ?? 0,
      result.summary?.criticalIssues ?? 0,
      result.summary?.warningIssues ?? 0,
      formatOptionalPath(result.reportPaths?.json),
      formatOptionalPath(result.reportPaths?.markdown),
      formatOptionalPath(result.reportPaths?.annotatedScreenshot)
    ].join(' | ').replace(/^/, '| ').replace(/$/, ' |'));
  });

  const warningLines = batch.results.flatMap((result) => (
    (result.warnings || []).map((warning) => `- \`${result.theme}\`: ${warning}`)
  ));

  return [
    '# Contrast Theme Batch Audit Report',
    '',
    `**URL:** ${batch.pageUrl}`,
    `**Date:** ${batch.generatedAt}`,
    `**Themes:** ${(batch.executedThemes || []).join(', ')}`,
    `**Original Theme Restored:** ${batch.restoredOriginalTheme ? 'Yes' : 'No'}`,
    batch.restoreError ? `**Restore Error:** ${batch.restoreError}` : '',
    '',
    '## Results',
    '',
    rows.join('\n'),
    '',
    '## Warnings',
    '',
    warningLines.length > 0 ? warningLines.join('\n') : 'No warnings.',
    ''
  ].filter(Boolean).join('\n');
}

function normalizeFinding(issue) {
  return {
    tag: issue.tag || issue.element || 'unknown',
    text: issue.text || issue.textContent || '',
    color: issue.color || issue.textColor || 'unknown',
    backgroundColor: issue.backgroundColor || issue.background || 'unknown',
    fontSize: issue.fontSize || 'unknown',
    fontWeight: issue.fontWeight || 'unknown',
    contrastRatio: issue.contrastRatio || issue.estimatedRatio || 'unknown',
    wcagAA: Boolean(issue.wcagAA),
    wcagAAA: Boolean(issue.wcagAAA),
    severity: issue.severity || 'warning',
    boundingBox: issue.boundingBox,
    recommendation: issue.recommendation || defaultRecommendation(issue)
  };
}

function formatFindingsTable(findings) {
  if (findings.length === 0) {
    return 'No issues found.';
  }

  const rows = [
    '| # | Element | Text | Color | Background | Font Size | Ratio | WCAG AA | Recommendation |',
    '|---|---------|------|-------|------------|-----------|-------|---------|----------------|'
  ];

  findings.forEach((finding, index) => {
    rows.push([
      index + 1,
      `\`${escapeTableCell(finding.tag)}\``,
      escapeTableCell(finding.text),
      escapeTableCell(finding.color),
      escapeTableCell(finding.backgroundColor),
      escapeTableCell(finding.fontSize),
      escapeTableCell(`${finding.contrastRatio}:1`),
      finding.wcagAA ? 'Pass' : 'Fail',
      escapeTableCell(finding.recommendation)
    ].join(' | ').replace(/^/, '| ').replace(/$/, ' |'));
  });

  return rows.join('\n');
}

function escapeTableCell(value) {
  return String(value ?? '')
    .replace(/\s+/g, ' ')
    .replaceAll('|', '\\|')
    .trim();
}

function formatOptionalPath(value) {
  return value ? `\`${value}\`` : '-';
}

function defaultRecommendation(issue) {
  if (issue.severity === 'critical') {
    return 'Increase contrast to at least 4.5:1 for normal text or 3:1 for large text.';
  }

  return 'Adjust foreground or background color to meet WCAG AA contrast thresholds.';
}

function safeHostName(pageUrl) {
  try {
    return new URL(pageUrl).hostname.replaceAll('.', '-').replace(/[^a-zA-Z0-9-]/g, '-');
  } catch {
    return 'unknown-page';
  }
}
